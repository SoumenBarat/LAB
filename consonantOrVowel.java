package JavaProgram;

public class consonantOrVowel {

	    public static void main(String[] args) {

	        char k = '9';

	        if(k == 'a' || k == 'e' || k == 'i' || k == 'o' || k == 'u' )
	            System.out.println(k+ " is vowel");
	        else
	            System.out.println(k+ " is consonant");

	    }
	}


